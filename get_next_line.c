#include "get_next_line.h"

char	*get_next_line(int fd)
{
	static char	buffer[BUFFER_SIZE + 1];
	char		*save;

	if (ft_checkbuff(buffer) == 0)
		read(fd, buffer, BUFFER_SIZE);
	
	
	return (save);
}

